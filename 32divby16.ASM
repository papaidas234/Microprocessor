DATA SEGMENT
DIVISOR DW 0010H
DIVIDEND DW 3456H,2335H
QUOTIENT DW 2 DUP(0)
REMAINDER DW 0
TEMP DW ?
TEMP1 DW ?
COUNT DW 10H
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START:
MOV AX,DATA
MOV DS,AX

CMP DIVISOR,0
JZ ERROR

MOV AX,DIVIDEND
MOV DX,DIVIDEND+2
MOV CX,DIVISOR

MOV BX,AX
MOV AX,DX
DIV CX
MOV REMAINDER,DX
MOV TEMP,AX

MOV AX,BX
DIV CX
MOV CX,DX
MOV TEMP1,AX
MOV AX,REMAINDER
MUL COUNT
MOV REMAINDER,AX
MOV AX,TEMP1
ADD AH,BYTE PTR[REMAINDER]
MOV BX,TEMP
MOV REMAINDER,DX
MOV QUOTIENT,AX
MOV QUOTIENT+2,DX
ERROR:	STC
MOV AX,4C00H
INT 21H
CODE ENDS
END START
END